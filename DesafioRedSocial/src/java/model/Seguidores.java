public class Seguidores {
    private int id;
    private int perfilSeguido;
    private int perfilSeguidor;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPerfilSeguido() {
        return perfilSeguido;
    }

    public void setPerfilSeguido(int perfilSeguido) {
        this.perfilSeguido = perfilSeguido;
    }

    public int getPerfilSeguidor() {
        return perfilSeguidor;
    }

    public void setPerfilSeguidor(int perfilSeguidor) {
        this.perfilSeguidor = perfilSeguidor;
    }
    
}
